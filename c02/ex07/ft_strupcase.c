char	*ft_strupcase(char *str)
{
	char *str2;

	str2 = str;
	while (*str2)
	{
		if (*str2 >= 'a' && *str2 <= 'z')
			*str2 = (*str2 - 32);
		str2++;
	}
	return (str);
}
