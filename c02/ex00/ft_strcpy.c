char	*ft_strcpy(char *dest, char *src)
{
	char *dest2;
	char *src2;

	dest2 = dest;
	src2 = src;
	while (*src2 != '\0')
	{
		*dest2 = *src2;
		src2++;
		dest2++;
	}
	*dest2 = '\0';
	return (dest);
}
