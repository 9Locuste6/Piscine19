int	ft_str_is_alpha(char *str)
{
	char *str2;

	str2 = str;
	while (*str2)
	{
		if ((*str2 < 'a' || *str2 > 'z') && (*str2 < 'A' || *str2 > 'Z'))
			return (0);
		str2++;
	}
	return (1);
}
