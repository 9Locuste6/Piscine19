int	ft_sqrt(int nb)
{
	long sqrt;

	if (nb % 2)
		sqrt = 1;
	else
		sqrt = 2;
	while (sqrt * sqrt < nb)
		sqrt += 2;
	if (sqrt * sqrt == nb)
		return (sqrt);
	return (0);
}
