char	*ft_strcat(char *dest, char *src)
{
	char *dest2;

	dest2 = dest;
	while (*dest2)
		dest2++;
	while (*src)
	{
		*dest2 = *src;
		src++;
		dest2++;
	}
	*dest2 = '\0';
	return (dest);
}
